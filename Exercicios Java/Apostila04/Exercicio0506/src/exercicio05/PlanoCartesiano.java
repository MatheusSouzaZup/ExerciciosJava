
package exercicio05;


public class PlanoCartesiano {
    int x, y;
    
    public PlanoCartesiano(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    //Get e Set
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
